"""Build OOD evaluation set for v105.10: values in [10000, 99999].

The standard train/test sets generated by `build_factor_graph_data_loguniform.py`
sample operands log-uniformly in [1, 9999] (LOG_HI=4.0). v105.8's 200-bin codebook
(50 log-spaced bins per decade in [100, 9999]) has NO bins above 9999, so any
gold value in [10000, 99999] is guaranteed to be misclassified by the per-NUMBER
readout.

v105.10's killer experiment: the AR digit decoder reads each digit independently
from the same cell_hidden the number readout uses. If cell_hidden carries enough
information to extract per-position digits, the digit decoder can attempt 5-digit
numbers it never saw during training — even though the number codebook fails.

This script generates factor graphs where ALL gold values fall in [10000, 99999]
by raising the operand sampling floor and reusing the rest of the loguniform
generator's machinery (DAG construction, validity checks, verification).

Output: .cache/factor_graph_test_ood_5digit.jsonl (default ~500 records).

Usage:
  .venv/bin/python scripts/build_factor_graph_ood_eval.py
  .venv/bin/python scripts/build_factor_graph_ood_eval.py --n 1000
"""
from __future__ import annotations

import argparse
import math
import os
import random
import sys
import time
from collections import defaultdict

# Make the sibling import work when invoked as a script.
sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))

import build_factor_graph_data_loguniform as bld  # noqa: E402


# ---------------------------------------------------------------------------
# OOD-specific operand sampler
# ---------------------------------------------------------------------------

OOD_LO  = 10_000   # minimum operand value
OOD_HI  = 99_999   # maximum operand / gold value (5 digits)
LOG_LO_OOD = math.log10(OOD_LO)      # 4.0
LOG_HI_OOD = math.log10(OOD_HI + 1)  # log10(100000) = 5.0


def _sample_loguniform_ood(rng: random.Random, lo: float = LOG_LO_OOD,
                            hi: float = LOG_HI_OOD) -> int:
    """Log-uniform on [10^lo, 10^hi); minimum returned is OOD_LO."""
    u = rng.uniform(lo, hi)
    v = int(round(10.0 ** u))
    return max(OOD_LO, v)


def _sample_ood_pair(rng: random.Random, op: str):
    """Sample (a, b, result) for op s.t. a, b, result are all in [OOD_LO, OOD_HI].

    Restricting operands AND results to OOD range guarantees every gold value
    (leaf and intermediate) lives in [10000, 99999].
    """
    for _ in range(400):
        if op == "add":
            # a + b in [OOD_LO, OOD_HI] requires both small. Sample a in
            # [OOD_LO, OOD_HI-OOD_LO], b in [OOD_LO, OOD_HI-a].
            a = _sample_loguniform_ood(rng)
            max_b = OOD_HI - a
            if max_b < OOD_LO:
                continue
            log_max = math.log10(max_b + 1)
            if log_max <= LOG_LO_OOD:
                continue
            b = _sample_loguniform_ood(rng, LOG_LO_OOD, log_max)
        elif op == "sub":
            # a - b in [OOD_LO, OOD_HI]; a in [2*OOD_LO, OOD_HI], b in
            # [OOD_LO, a - OOD_LO].
            min_a = 2 * OOD_LO
            if min_a > OOD_HI:
                continue
            a = _sample_loguniform_ood(rng, math.log10(min_a), LOG_HI_OOD)
            max_b = a - OOD_LO
            if max_b < OOD_LO:
                continue
            log_max = math.log10(max_b + 1)
            if log_max <= LOG_LO_OOD:
                continue
            b = _sample_loguniform_ood(rng, LOG_LO_OOD, log_max)
        elif op == "mul":
            # a * b in [OOD_LO, OOD_HI] with both >= OOD_LO ⇒ product >= 10^8.
            # Impossible to stay in [10000, 99999] if BOTH operands are >= 10000.
            # Skip multiplication entirely for OOD set (it would force operands
            # outside the OOD range).
            return None
        elif op == "div":
            # a / b in [OOD_LO, OOD_HI] with b >= OOD_LO ⇒ a >= b*OOD_LO >= 10^8.
            # Also impossible to keep a in [10000, 99999]. Skip.
            return None
        else:
            return None
        r = bld.apply_op(op, a, b)
        if r is not None and OOD_LO <= r <= OOD_HI:
            return a, b, r
    return None


def _find_valid_a_ood(rng: random.Random, op: str, b: int):
    for _ in range(400):
        if op == "add":
            max_a = OOD_HI - b
            if max_a < OOD_LO:
                return None
            log_max = math.log10(max_a + 1)
            if log_max <= LOG_LO_OOD:
                return None
            a = _sample_loguniform_ood(rng, LOG_LO_OOD, log_max)
        elif op == "sub":
            min_a = b + OOD_LO
            if min_a > OOD_HI:
                return None
            a = _sample_loguniform_ood(rng, math.log10(min_a), LOG_HI_OOD)
        elif op == "mul" or op == "div":
            return None
        else:
            return None
        r = bld.apply_op(op, a, b)
        if r is not None and OOD_LO <= r <= OOD_HI:
            return a, r
    return None


def _find_valid_b_ood(rng: random.Random, op: str, a: int):
    for _ in range(400):
        if op == "add":
            max_b = OOD_HI - a
            if max_b < OOD_LO:
                return None
            log_max = math.log10(max_b + 1)
            if log_max <= LOG_LO_OOD:
                return None
            b = _sample_loguniform_ood(rng, LOG_LO_OOD, log_max)
        elif op == "sub":
            max_b = a - OOD_LO
            if max_b < OOD_LO:
                return None
            log_max = math.log10(max_b + 1)
            if log_max <= LOG_LO_OOD:
                return None
            b = _sample_loguniform_ood(rng, LOG_LO_OOD, log_max)
        elif op == "mul" or op == "div":
            return None
        else:
            return None
        r = bld.apply_op(op, a, b)
        if r is not None and OOD_LO <= r <= OOD_HI:
            return b, r
    return None


# ---------------------------------------------------------------------------
# OOD record generator (mirrors bld.generate_record but uses OOD samplers
# and restricts ops to add/sub only — mul/div force operands outside [10k, 99k]).
# ---------------------------------------------------------------------------

OOD_OPS = ("add", "sub")


def generate_record_ood(rng: random.Random, n_leaves: int, difficulty: str):
    use_tree = (n_leaves >= 4) and rng.random() < 0.5
    if use_tree:
        n_total, factor_args = bld.make_tree_dag(rng, n_leaves)
    else:
        n_total, factor_args = bld.make_chain_dag(n_leaves)
    n_factors = len(factor_args)

    topo_order = bld.topo_sort(n_total, factor_args)
    if topo_order is None:
        return None

    result_set = {fa[2] for fa in factor_args}
    leaf_set = set(range(n_total)) - result_set
    result_to_fi = {fa[2]: fi for fi, fa in enumerate(factor_args)}

    factor_types = [rng.choice(OOD_OPS) for _ in range(n_factors)]

    gold = [None] * n_total

    # Leaves start in OOD range
    for i in leaf_set:
        gold[i] = _sample_loguniform_ood(rng)

    for node in topo_order:
        if node not in result_to_fi:
            continue
        fi = result_to_fi[node]
        a_idx, b_idx, r_idx = factor_args[fi]
        op = factor_types[fi]
        a_val = gold[a_idx]
        b_val = gold[b_idx]
        if a_val is None or b_val is None:
            return None

        r = bld.apply_op(op, a_val, b_val)
        if r is None or not (OOD_LO <= r <= OOD_HI):
            a_is_leaf = a_idx in leaf_set
            b_is_leaf = b_idx in leaf_set
            if a_is_leaf and b_is_leaf:
                pair = _sample_ood_pair(rng, op)
                if pair is None:
                    return None
                gold[a_idx], gold[b_idx], r = pair
            elif a_is_leaf:
                fix = _find_valid_a_ood(rng, op, b_val)
                if fix is None:
                    return None
                gold[a_idx], r = fix
            elif b_is_leaf:
                fix = _find_valid_b_ood(rng, op, a_val)
                if fix is None:
                    return None
                gold[b_idx], r = fix
            else:
                return None

        if r is None or not (OOD_LO <= r <= OOD_HI):
            return None
        gold[r_idx] = r

    if any(v is None for v in gold):
        return None
    if any(not (OOD_LO <= v <= OOD_HI) for v in gold):
        return None

    observed_mask = [1 if i in leaf_set else 0 for i in range(n_total)]
    observed_values = [
        gold[i] if observed_mask[i] == 1 else None for i in range(n_total)
    ]

    result_nodes_topo = [n for n in topo_order if n in result_set]
    if not result_nodes_topo:
        return None
    query_idx = result_nodes_topo[-1]

    query_answer = gold[query_idx]
    obs_vals = {v for v in observed_values if v is not None}
    if query_answer in obs_vals and n_leaves <= 3:
        return None

    return {
        "n_vars":         n_leaves,
        "n_factors":      n_factors,
        "factor_types":   factor_types,
        "factor_args":    factor_args,
        "observed_mask":  observed_mask,
        "observed_values": observed_values,
        "gold_values":    gold,
        "query_idx":      query_idx,
        "difficulty":     difficulty,
    }


def generate_record_with_retry_ood(rng: random.Random, n_leaves: int, difficulty: str):
    for _ in range(bld.MAX_RETRIES_PER_RECORD):
        rec = generate_record_ood(rng, n_leaves, difficulty)
        if rec is not None:
            return rec
    return None


# ---------------------------------------------------------------------------
# Build
# ---------------------------------------------------------------------------

def build_ood(n_target: int, seed: int):
    rng = random.Random(seed)
    plan = {
        "easy":   n_target // 3,
        "medium": n_target // 3,
        "hard":   n_target - 2 * (n_target // 3),
    }
    print(f"[ood] plan: {plan} (total {n_target})", flush=True)

    records = []
    counts = defaultdict(int)
    t0 = time.time()
    for diff, n in plan.items():
        lo, hi = bld.DIFFICULTY_BANDS[diff]
        for _ in range(n):
            n_leaves = rng.randint(lo, hi)
            rec = generate_record_with_retry_ood(rng, n_leaves, diff)
            if rec is not None:
                records.append(rec)
                counts[diff] += 1
    print(f"[ood] generated {len(records)}/{n_target} records in "
          f"{time.time()-t0:.1f}s {dict(counts)}", flush=True)
    return records


def verify_record_ood(rec: dict) -> bool:
    """Standard arithmetic correctness check + OOD range check on every gold."""
    if not bld.verify_record(rec):
        return False
    for v in rec["gold_values"]:
        if not (OOD_LO <= v <= OOD_HI):
            return False
    return True


def write_records(records: list, out_path: str, seed: int) -> None:
    import json
    rng = random.Random(seed + 77)
    rng.shuffle(records)
    with open(out_path, "w") as f:
        for rec in records:
            f.write(json.dumps(rec) + "\n")


def print_ood_stats(records: list) -> None:
    n = len(records)
    print(f"\n=== OOD stats ({n} records) ===", flush=True)
    diff_counts = defaultdict(int)
    op_counts = defaultdict(int)
    val_min = None
    val_max = None
    for rec in records:
        diff_counts[rec["difficulty"]] += 1
        for op in rec["factor_types"]:
            op_counts[op] += 1
        for v in rec["gold_values"]:
            if val_min is None or v < val_min:
                val_min = v
            if val_max is None or v > val_max:
                val_max = v
    print(f"  difficulty: {dict(diff_counts)}")
    print(f"  ops:        {dict(op_counts)}")
    print(f"  value range: [{val_min}, {val_max}]")


# ---------------------------------------------------------------------------
# Entry
# ---------------------------------------------------------------------------

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default=".cache/factor_graph_test_ood_5digit.jsonl")
    ap.add_argument("--n",   type=int, default=500)
    ap.add_argument("--seed", type=int, default=2510)
    args = ap.parse_args()

    print(f"[main] OOD factor graph builder")
    print(f"  out:          {args.out}")
    print(f"  n:            {args.n}")
    print(f"  seed:         {args.seed}")
    print(f"  value range:  [{OOD_LO}, {OOD_HI}] (5-digit OOD)")
    print(f"  ops:          {OOD_OPS} (mul/div skipped — incompatible)")
    print()

    os.makedirs(os.path.dirname(args.out) or ".", exist_ok=True)

    records = build_ood(args.n, args.seed)

    print(f"\n[verify] checking arithmetic correctness AND OOD range...")
    n_check = min(100, len(records))
    rng = random.Random(999)
    sample = rng.sample(records, n_check)
    failures = [i for i, r in enumerate(sample) if not verify_record_ood(r)]
    if failures:
        raise AssertionError(
            f"verification failed on {len(failures)}/{n_check}; "
            f"first failure index: {failures[0]}"
        )
    print(f"[verify] PASSED ({n_check} samples)", flush=True)

    print_ood_stats(records)

    write_records(records, args.out, args.seed)
    print(f"\n[main] wrote {len(records)} records → {args.out}", flush=True)


if __name__ == "__main__":
    main()
